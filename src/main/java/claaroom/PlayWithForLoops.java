package claaroom;

public class PlayWithForLoops {
    public static void main(String[] args) {
        int[] numbers = {2, 20, 30, 40, 50, 55, 100};
        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[1]);
        }
        for (int i = 10; i >= 1; i--) {
            System.out.print(i);
        }


    }
}





